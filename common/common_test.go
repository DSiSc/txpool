package common

import (
	"github.com/DSiSc/craft/types"
	"github.com/stretchr/testify/assert"
	"math/big"
	"testing"
)

var emptyTx *types.Transaction

func TestNewTransaction(t *testing.T) {
	assert := assert.New(t)
	b := types.Address{
		0xb2, 0x6f, 0x2b, 0x34, 0x2a, 0xab, 0x24, 0xbc, 0xf6, 0x3e,
		0xa2, 0x18, 0xc6, 0xa9, 0x27, 0x4d, 0x30, 0xab, 0x9a, 0x15,
	}
	emptyTx = NewTransaction(
		0,
		b,
		big.NewInt(0),
		0,
		big.NewInt(0),
		b[:10],
		b,
	)
	assert.NotNil(emptyTx)
	assert.Equal(emptyTx.Data.From, &b)
	assert.Equal(emptyTx.Data.Recipient, &b)
	assert.Equal(emptyTx.Data.AccountNonce, uint64(0))
	assert.Equal(emptyTx.Data.GasLimit, uint64(0))
	assert.Equal(emptyTx.Data.Price, big.NewInt(0))
}

func TestTxHash(t *testing.T) {
	assert := assert.New(t)
	hash := TxHash(emptyTx)
	expect := types.Hash{
		0x5f, 0xd5, 0x56, 0xa1, 0x56, 0x50, 0xcd, 0x19, 0xa2, 0xa, 0xd1, 0xa, 0xca, 0x46, 0xfe, 0xab,
		0xdd, 0xb1, 0x1c, 0x3f, 0xa4, 0x99, 0x10, 0x9b, 0x98, 0xf9, 0x15, 0x10, 0xdb, 0x44, 0xf0, 0x15,
	}
	assert.Equal(expect, hash)

	hash = TxHash(nil)
	var exceptNilTxHash types.Hash
	assert.Equal(exceptNilTxHash, hash)
}

func TestSum(t *testing.T) {
	b := []byte{
		0xb2, 0x6f, 0x2b, 0x34, 0x2a, 0xab, 0x24, 0xbc, 0xf6, 0x3e,
		0xa2, 0x18, 0xc6, 0xa9, 0x27, 0x4d, 0x30, 0xab, 0x9a, 0x15,
	}
	c := Sum(b)
	except := []byte{
		0x5b, 0x49, 0xff, 0xa2, 0x47, 0x5e, 0x11, 0x40, 0x92, 0xb,
		0x4, 0x6, 0x43, 0xc5, 0x7f, 0x8e, 0x5b, 0xb1, 0x86, 0x4, 0xeb,
		0xcc, 0x62, 0x43, 0x71, 0x39, 0x76, 0xfd, 0xa4, 0x8b, 0xc3, 0x7}
	assert.Equal(t, except, c)
}

func TestCopyBytes(t *testing.T) {
	s := CopyBytes(nil)
	assert.Nil(t, s)
	b := []byte{
		0xb2, 0x6f, 0x2b, 0x34, 0x2a, 0xab, 0x24, 0xbc, 0xf6, 0x3e,
		0xa2, 0x18, 0xc6, 0xa9, 0x27, 0x4d, 0x30, 0xab, 0x9a, 0x15,
	}
	c := CopyBytes(b)
	assert.Equal(t, b, c)
}
